// How the work was handed off at completion.
export type VisitFulfilmentMethod = 'in_person' | 'shipping';

export interface CompleteVisitInput {
  patient_id: string;
  visit_id: string;
  appointment_id: string | null;
  walk_in_id: string | null;
  fulfilment_method: VisitFulfilmentMethod;
  // Total in pence — used purely for the timeline event payload so
  // the closed event reads "£298.00" without a join back to the
  // cart roll-up.
  total_pence?: number;
}

// Explicit visit completion. Until now Pay auto-completed the visit
// when staff sent the receipt; that conflated payment with hand-off
// and skipped the in-person-vs-shipping question. Now staff has to
// click Complete visit on VisitDetail and answer the fulfilment
// question, which lands as fulfilment_method on the visit row for a
// future shipping flow to pick up.
//
// Side effects:
//   - flips lng_visits.status='complete' + stamps closed_at
//   - records fulfilment_method (in_person | shipping)
//   - frees the job box on the appointment / walk_in source so the
//     box becomes available for the next booking (the visit's own
//     jb_ref column is captured at insert and stays as audit)
//   - writes a patient_events 'visit_closed' row with the staff
//     account and the fulfilment choice on the payload
export async function completeVisit(input: CompleteVisitInput): Promise<void> {
  const { data: accountId } = await supabase.rpc('auth_account_id');

  const { error: visitErr } = await supabase
    .from('lng_visits')
    .update({
      status: 'complete',
      closed_at: new Date().toISOString(),
      fulfilment_method: input.fulfilment_method,
    })
    .eq('id', input.visit_id);
  if (visitErr) throw new Error(visitErr.message);

  // Free the JB on the source row. The visit's own captured jb_ref
  // is immutable and survives this clearing, so the timeline +
  // history stay intact.
  if (input.appointment_id) {
    await supabase
      .from('lng_appointments')
      .update({ jb_ref: null })
      .eq('id', input.appointment_id);
  }
  if (input.walk_in_id) {
    await supabase
      .from('lng_walk_ins')
      .update({ jb_ref: null })
      .eq('id', input.walk_in_id);
  }

  await supabase.from('patient_events').insert({
    patient_id: input.patient_id,
    event_type: 'visit_closed',
    actor_account_id: (accountId as string | null) ?? null,
    payload: {
      visit_id: input.visit_id,
      total_pence: input.total_pence ?? null,
      fulfilment_method: input.fulfilment_method,
      staff_account_id: (accountId as string | null) ?? null,
    },
  });
}

// Single entry point for the cart-line Remove sheet. Handles all
// three reason categories in one call: soft-deletes the line,
// writes the right audit row(s), and decides whether the visit
// should terminate as a result.
//
// Termination rule: visit flips to status='unsuitable' when the
// cart becomes empty AND at least one unsuitability record exists
// on this visit (in this removal or a prior one). Mixed visits
// where some removals were mistakes and at least one was
// unsuitable still terminate. Cart-empty-by-mistake-only visits
// stay active so staff can re-add.
export interface RemoveCartLineWithReasonInput {
  cart_item_id: string;
  catalogue_id: string | null; // required for 'unsuitable'
  visit_id: string;
  patient_id: string;
  reason: 'mistake' | 'changed_mind' | 'unsuitable';
  note?: string;
}

export async function removeCartLineWithReason(
  input: RemoveCartLineWithReasonInput
): Promise<{ visit_terminated: boolean }> {
  const note = input.note?.trim() ?? '';
  if (input.reason === 'unsuitable' && note.length === 0) {
    throw new Error('A reason is required for unsuitable removals');
  }
  if (input.reason === 'unsuitable' && !input.catalogue_id) {
    throw new Error('Unsuitable removal requires a catalogue-backed line');
  }

  const { data: accountId } = await supabase.rpc('auth_account_id');

  // Read the line's display name BEFORE soft-deleting so the
  // patient_events row can carry "Removed [name]" without a join
  // back to lng_cart_items at read time. The frozen catalogue
  // snapshot on the cart row is the source of truth (matches what
  // staff saw in the cart at removal time).
  const { data: lineRow, error: lineErr } = await supabase
    .from('lng_cart_items')
    .select('name')
    .eq('id', input.cart_item_id)
    .maybeSingle();
  if (lineErr) throw new Error(lineErr.message);
  const lineName = (lineRow as { name: string } | null)?.name ?? null;

  // 1. Soft-delete the cart line. Writes the cart-item-level audit.
  const { error: removeErr } = await supabase
    .from('lng_cart_items')
    .update({
      removed_at: new Date().toISOString(),
      removed_reason: input.reason,
      removed_by: (accountId as string | null) ?? null,
      removed_note: note.length > 0 ? note : null,
    })
    .eq('id', input.cart_item_id);
  if (removeErr) throw new Error(removeErr.message);

  // 2. Reason-specific audit:
  if (input.reason === 'unsuitable' && input.catalogue_id) {
    // Immutable per-product unsuitability record. Used by the
    // header to render "By [name]" and by future "this patient was
    // previously unsuitable for this product" warnings.
    const { error: recErr } = await supabase.from('lng_unsuitability_records').insert({
      patient_id: input.patient_id,
      visit_id: input.visit_id,
      catalogue_id: input.catalogue_id,
      reason: note,
      recorded_by: (accountId as string | null) ?? null,
    });
    if (recErr) throw new Error(recErr.message);
  }

  // Timeline event for every removal regardless of reason. The
  // event_type carries the category so a downstream consumer can
  // bucket without re-reading the cart row.
  await supabase.from('patient_events').insert({
    patient_id: input.patient_id,
    event_type: 'cart_line_removed',
    actor_account_id: (accountId as string | null) ?? null,
    notes: note.length > 0 ? note : null,
    payload: {
      visit_id: input.visit_id,
      cart_item_id: input.cart_item_id,
      catalogue_id: input.catalogue_id,
      // Line's display name at removal time. Lets the timeline
      // render "Removed [name]" without joining back to cart_items.
      line_name: lineName,
      reason: input.reason,
      note: note.length > 0 ? note : null,
    },
  });

  // 3. Termination check. Visit flips to 'unsuitable' when the cart
  //    is empty AND at least one unsuitability record exists on the
  //    visit. Cart-empty-by-mistake-only visits stay active.
  const { data: cartRow } = await supabase
    .from('lng_carts')
    .select('id')
    .eq('visit_id', input.visit_id)
    .maybeSingle();
  if (!cartRow?.id) return { visit_terminated: false };

  const { count: activeCount, error: countErr } = await supabase
    .from('lng_cart_items')
    .select('id', { count: 'exact', head: true })
    .eq('cart_id', cartRow.id)
    .is('removed_at', null);
  if (countErr) throw new Error(countErr.message);

  if ((activeCount ?? 0) > 0) return { visit_terminated: false };

  const { count: unsuitableCount, error: unsErr } = await supabase
    .from('lng_unsuitability_records')
    .select('id', { count: 'exact', head: true })
    .eq('visit_id', input.visit_id);
  if (unsErr) throw new Error(unsErr.message);
  if ((unsuitableCount ?? 0) === 0) return { visit_terminated: false };

  // Stamp the visit-level end reason/note alongside the status flip
  // so the new constraint (status='unsuitable' requires reason+note)
  // holds. The triggering removal's note becomes the visit-level
  // note — it's the most recent staff input and the one that ended
  // the visit.
  const { error: visitErr } = await supabase
    .from('lng_visits')
    .update({
      status: 'unsuitable',
      closed_at: new Date().toISOString(),
      visit_end_reason: 'unsuitable',
      visit_end_note: note,
    })
    .eq('id', input.visit_id);
  if (visitErr) throw new Error(visitErr.message);

  return { visit_terminated: true };
}

// Reverses either visit-level terminus ('unsuitable' OR
// 'ended_early') AND restores every soft-deleted cart line on this
// visit (regardless of removal reason). lng_unsuitability_records
// rows stay as audit — we never mutate signed-off records — but the
// cart-item soft-delete is un-flagged so the cart returns exactly
// as it was before the removal sequence began. visit_end_reason
// and visit_end_note also clear so the constraint stays satisfied.
//
// "Restore everything" is deliberate per Dylan's call: a "wrong
// product" mistake reappears alongside the unsuitable lines, and
// staff has to re-remove it. The trade-off buys a clean mental
// model — Reverse really resets this visit, no surprises about
// which lines come back.
export async function reverseUnsuitability(input: ReverseUnsuitabilityInput): Promise<void> {
  const { data: accountId } = await supabase.rpc('auth_account_id');

  // Find the visit's cart so we can target its lines for un-flag.
  const { data: cartRow, error: cartErr } = await supabase
    .from('lng_carts')
    .select('id')
    .eq('visit_id', input.visit_id)
    .maybeSingle();
  if (cartErr) throw new Error(cartErr.message);

  // Un-flag every soft-deleted line on this cart. Belt-and-braces
  // null-out of the trio so a future direct read can't see a
  // half-restored row. No-op when the visit has no cart yet (rare;
  // would mean a visit was marked unsuitable before any items were
  // ever added).
  if (cartRow?.id) {
    const { error: restoreErr } = await supabase
      .from('lng_cart_items')
      .update({ removed_at: null, removed_reason: null, removed_by: null, removed_note: null })
      .eq('cart_id', cartRow.id)
      .not('removed_at', 'is', null);
    if (restoreErr) throw new Error(restoreErr.message);
  }

  const { error: visitErr } = await supabase
    .from('lng_visits')
    .update({
      status: 'in_chair',
      closed_at: null,
      visit_end_reason: null,
      visit_end_note: null,
    })
    .eq('id', input.visit_id);
  if (visitErr) throw new Error(visitErr.message);

  await supabase.from('patient_events').insert({
    patient_id: input.patient_id,
    event_type: 'patient_unsuitable_reversed',
    actor_account_id: (accountId as string | null) ?? null,
    payload: {
      visit_id: input.visit_id,
      staff_account_id: (accountId as string | null) ?? null,
    },
  });
}

// Latest end-of-visit details (either path: 'unsuitable' or
// 'ended_early'). The header lifecycle line uses these to render
// "Marked Unsuitable [time] / By [name]" or "Visit Ended Early
// [time] / By [name]". Null while the visit is still active.
//
// For 'unsuitable' the staff name comes from lng_unsuitability_records
// (the latest record's recorded_by). For 'ended_early' it comes
// from the latest patient_events row of type 'visit_ended_early'
// matching this visit.
export interface LatestUnsuitability {
  recorded_at: string;
  // The reason text shown beneath the header line. For unsuitable
  // it's the lng_unsuitability_records.reason from the latest
  // record; for ended_early it's lng_visits.visit_end_note.
  reason: string;
  recorded_by_name: string | null;
  // Discriminator so the header can render the right label.
  category: 'unsuitable' | 'ended_early';
  // Sub-category for 'ended_early' visits (patient_declined,
  // patient_walked_out, wrong_booking, other). Null when category
  // is 'unsuitable'.
  end_reason: VisitEndReason | null;
}

interface LatestUnsuitabilityResult {
  data: LatestUnsuitability | null;
  loading: boolean;
  error: string | null;
}

interface AccountJoin {
  first_name: string | null;
  last_name: string | null;
  name: string | null;
}

function displayAccountName(a: AccountJoin | AccountJoin[] | null | undefined): string | null {
  const flat = Array.isArray(a) ? a[0] ?? null : a ?? null;
  if (!flat) return null;
  const fn = flat.first_name?.trim();
  const ln = flat.last_name?.trim();
  if (fn && ln) return `${fn} ${ln}`;
  return fn ?? ln ?? flat.name?.trim() ?? null;
}

export function useLatestUnsuitability(visitId: string | null): LatestUnsuitabilityResult {
  const [data, setData] = useState<LatestUnsuitability | null>(null);
  const [error, setError] = useState<string | null>(null);
  const { loading, settle } = useStaleQueryLoading(visitId);

  useEffect(() => {
    if (!visitId) {
      setData(null);
      settle();
      return;
    }
    let cancelled = false;
    (async () => {
      // Step A: read the visit row to learn status + end reason/note.
      // Visits not in a terminal status return null — the header
      // shouldn't surface anything until the visit ends.
      const { data: vRow, error: vErr } = await supabase
        .from('lng_visits')
        .select('status, closed_at, visit_end_reason, visit_end_note')
        .eq('id', visitId)
        .maybeSingle();
      if (cancelled) return;
      if (vErr) {
        setError(vErr.message);
        settle();
        return;
      }
      const v = vRow as {
        status: string | null;
        closed_at: string | null;
        visit_end_reason: VisitEndReason | null;
        visit_end_note: string | null;
      } | null;
      if (!v || (v.status !== 'unsuitable' && v.status !== 'ended_early')) {
        setData(null);
        settle();
        return;
      }

      // Step B: pull the staff name. For 'unsuitable' we have the
      // recorded_by FK on the latest lng_unsuitability_records row;
      // for 'ended_early' it lives on the patient_events row this
      // function emits.
      if (v.status === 'unsuitable') {
        const { data: row, error: err } = await supabase
          .from('lng_unsuitability_records')
          .select('recorded_at, reason, account:accounts!recorded_by ( first_name, last_name, name )')
          .eq('visit_id', visitId)
          .order('recorded_at', { ascending: false })
          .limit(1)
          .maybeSingle();
        if (cancelled) return;
        if (err) {
          if (err.code === '42P01' || err.code === 'PGRST200') {
            setData(null);
            setError(null);
          } else {
            setError(err.message);
          }
          settle();
          return;
        }
        const r = row as { recorded_at: string; reason: string; account: AccountJoin | AccountJoin[] | null } | null;
        if (!r) {
          // status='unsuitable' without a record is the migration-
          // backfilled case; fall back to the visit's own end note
          // and skip the staff name.
          setData({
            recorded_at: v.closed_at ?? new Date(0).toISOString(),
            reason: v.visit_end_note ?? '',
            recorded_by_name: null,
            category: 'unsuitable',
            end_reason: null,
          });
          settle();
          return;
        }
        setData({
          recorded_at: r.recorded_at,
          reason: r.reason,
          recorded_by_name: displayAccountName(r.account),
          category: 'unsuitable',
          end_reason: null,
        });
        settle();
        return;
      }

      // status === 'ended_early'
      const { data: peRow, error: peErr } = await supabase
        .from('patient_events')
        .select('created_at, actor_account_id, account:accounts!actor_account_id ( first_name, last_name, name )')
        .eq('event_type', 'visit_ended_early')
        .filter('payload->>visit_id', 'eq', visitId)
        .order('created_at', { ascending: false })
        .limit(1)
        .maybeSingle();
      if (cancelled) return;
      if (peErr && peErr.code !== '42P01' && peErr.code !== 'PGRST200') {
        setError(peErr.message);
        settle();
        return;
      }
      const pe = peRow as { created_at: string; account: AccountJoin | AccountJoin[] | null } | null;
      setData({
        recorded_at: pe?.created_at ?? v.closed_at ?? new Date(0).toISOString(),
        reason: v.visit_end_note ?? '',
        recorded_by_name: displayAccountName(pe?.account),
        category: 'ended_early',
        end_reason: v.visit_end_reason,
      });
      settle();
    })();
    return () => {
      cancelled = true;
    };
  }, [visitId, settle]);

  return { data, loading, error };
}
